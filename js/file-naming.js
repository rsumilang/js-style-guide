/**
 * Public JavaScript classes should be in a single .js file and match the name
 * of the class.
 *
 * @link https://github.com/rsumilang/js-style-guide/guides/file-naming.md
 */

// Right

// MyClass.js
var MyClass = function MyClass () {
  // magic.
};

// Wrong

// my-class.js
var MyClass = function MyClass () {
};